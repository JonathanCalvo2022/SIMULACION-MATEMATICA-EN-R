########################METODO CONGRUENCIAL###########################
m.cong <- function(x0,a,c,m,M){
  aux<-numeric(m)
  aux[1]<-x0
  for(i in 2:m){
    aux[i]<-(aux[i-1]*a+c)%%M
  }
  (aux/M)
} # m = 100
xi <- m.cong(x0 = 111, a= 13, c=7, m =100 ,M= 2^32);xi
